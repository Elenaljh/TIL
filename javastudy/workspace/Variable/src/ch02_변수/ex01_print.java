package ch02_변수;

public class ex01_print {
	public static void main(String args[]) {
		System.out.println("Hello, world");// 화면에 Hello, world가 출력하고 줄바꿈 한다. (ln때문)
		System.out.print("Hello"); // 화면에 Hello를 출력하고 줄바꿈 안한다.
		System.out.println("World"); // 화면에 World를 출력하고 줄바꿈 한다.
		//printf는? format 가능

		//문자:'', 문자열: ""

		// byte < short < int < long < float < double
		// char < int : char과 int끼리 계산시 int로 형변환됨
	}
}
